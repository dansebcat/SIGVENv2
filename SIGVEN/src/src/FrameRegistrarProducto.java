    void registrar() {
        if (!dao.existeProducto(txtNombre.getText())) {
            String nombre = txtNombre.getText();
            String tipo = txtTipo.getText();
            Double precio = Double.parseDouble(txtPrecio.getText());
            Object[] o = new Object[3];
            o[0] = nombre;
            o[1] = tipo;
            o[2] = precio;
            dao.add(o);
        }else{
            JOptionPane.showMessageDialog(null,"El producto ya se encuentra registrado - vuelva a ingresar");
        }

    }


