    void listar() {
        limpiarTabla();
        modelo = new DefaultTableModel();
        List<Producto> lista = dao.listar();
        modelo = (DefaultTableModel) tablaProducto.getModel();
        Object[] ob = new Object[4];
        for (int i = 0; i < lista.size(); i++) {
            ob[0] = lista.get(i).getCodproducto();
            ob[1] = lista.get(i).getNombre();
            ob[2] = lista.get(i).getTipo();
            ob[3] = lista.get(i).getPrecio();
            modelo.addRow(ob);
        }
        tablaProducto.setModel(modelo);
    }

    public void limpiarTabla() {
        DefaultTableModel tb = (DefaultTableModel) tablaProducto.getModel();
        int a = tablaProducto.getRowCount() - 1;
        for (int i = a; i >= 0; i--) {
            tb.removeRow(tb.getRowCount() - 1);
        }
        //cargaTicket();
    }

