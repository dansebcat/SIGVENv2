package src;

/**
 *
 * @author danie
 */
public class Producto {
    int codproducto;
    String nombre;
    String tipo;
    double precio;

    public Producto() {
    }

    public Producto(int codproducto, String nombre, String tipo, double precio) {
        this.codproducto = codproducto;
        this.nombre = nombre;
        this.tipo = tipo;
        this.precio = precio;
    }

    public int getCodproducto() {
        return codproducto;
    }

    public void setCodproducto(int codproducto) {
        this.codproducto = codproducto;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public double getPrecio() {
        return precio;
    }

    public void setPrecio(double precio) {
        this.precio = precio;
    }
    
}
